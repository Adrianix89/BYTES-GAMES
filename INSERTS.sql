CREATE TRIGGER TR_UPDATE_UNIT_PRICE
ON ORDER_DETAILS
AFTER INSERT
AS
BEGIN TRAN
	UPDATE ORDER_DETAILS
  		SET unitPrice = VIDEOGAMES.unitPrice
		FROM VIDEOGAMES
		WHERE ORDER_DETAILS.idGame = VIDEOGAMES.idGame
	IF @@ERROR <> 0
		BEGIN
			ROLLBACK TRAN
		END
COMMIT TRAN
GO
